//Valores máximo y mínimo de tipos: Escribe un programa que imprima los
//valores máximo y mínimo de int, double, y char usando métodos de las clases
//envolventes.
package CursoJava;
public class Ejercicio34 {
    
  public static void main(String[] args) {
      
    
    System.out.println("Máximo y mínimo valor para un tipo de dato int:");
    System.out.println(Integer.MAX_VALUE + " " + Integer.MIN_VALUE);
    System.out.println("");   
    System.out.println("Máximo y mínimo valor para un tipo de dato double:");
    System.out.println(Double.MAX_VALUE + " " + Double.MIN_VALUE);
    System.out.println("");   
    System.out.println("Máximo y mínimo valor para un tipo de dato char:");
    System.out.println(Character.MAX_VALUE + " " + Character.MIN_VALUE);
}
}



  


